package main;

/**
 * OPLCandidate.java
 * Represents an OPL candidate and its information
 * @author Team 7
 *
 */
public class OPLCandidate {
	public String name;
	public String party;
	/**
	 * constructor
	 * @param name candidate name
	 * @param party candidate party
	 */
	public OPLCandidate(String name, String party) {
		this.name = name;
		this.party = party;
	}
}
